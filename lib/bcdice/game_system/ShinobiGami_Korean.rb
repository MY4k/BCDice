      def result_2d6(total, dice_total, _dice_list, cmp_op, target)
        return nil unless cmp_op == :>=

        if dice_total <= 2
          Result.fumble("펌블")
        elsif dice_total >= 12
          Result.critical(SPECIAL)
        elsif target == "?"
          nil
        elsif total >= target
          Result.success("성공")
        else
          Result.failure("실패")
        end
      end

      register_prefix('\d*SG')

      def eval_game_system_specific_command(command)
        return action_roll(command) || repeat_table(command)
      end

      def repeat_table(command)
        times = command.start_with?(/\d/) ? command.to_i : 1
        key = command.sub(/^\d+/, '')
        results = [*0...times].map do |_|
          result = roll_tables(key, TABLES) || roll_tables(key, SCENE_TABLES) ||
            roll_tables(key, DEMON_SKILL_TABLES) || roll_tables(key, DEMON_SKILL_TABLES_NEW) || RTT.roll_command(@randomizer, key)
    
          # 일본어를 한국어로 치환
          if result
            result = result.gsub(/分野ランダム特技表/, " 분야 랜덤 특기 결정표")
                           .gsub(/ランダム特技表/, "랜덤 특기 결정표")
                           .gsub(/ランダム指定特技表/, "랜덤 지정 특기표")
                           .gsub(/ランダム分野表/, "랜덤 분야 표")  # RCT 테이블용 추가
          end
    
          result
        end.compact
        return nil if results.empty?

        return results.join("\n")
      end

